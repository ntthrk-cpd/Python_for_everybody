
salary = int(input("Enter your salary: "))
if salary > 50000:
    print("เงินเดือนมากกว่า 50000")
elif salary > 30000 and salary < 50000:
    print("เงินเดือนมากกว่า 30000 แต่น้อยกว่า 50000")
elif salary < 30000:
    print("เงินเดือนน้อยกว่า 30000")
else:
    print("ไม่เข้าเงื่อนไขใดๆ") # ถ้าใส่ค่าเป็น 50000 จะเข้าเงื่อนไขนี้ เพราะไม่เข้าเงื่อนไขตามโจทย์ที่กำหนดไว้